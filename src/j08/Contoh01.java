package j08;

public class Contoh01 {
    public static void main(String[] args)
    {
       // PERULANGAN FOR
        int x;
        for(x = 1; x <= 5; x++);
        {
            System.out.println("Telkom School Malang");
        }
        
        //PERULANGAN WHILE
        int i = 1;
        while(i <= 5);
        {
            System.out.println("Telkom School Malang");
            i++;
        }
        
        //PERULANGAN DO WHILE
        int j =1;
        do
        {
            System.out.println("Telkom School Malang");
            j++;
        }
        while(j <= 5);
    }
}
